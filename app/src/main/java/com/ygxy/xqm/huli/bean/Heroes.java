public class Heroes {
    private int heroes_range;
    private String heroes_name;
    private int heroes_grade;
    private String heroes_nickname;

    public int getHeroes_range() {
        return heroes_range;
    }

    public void setHeroes_range(int heroes_range) {
        this.heroes_range = heroes_range;
    }

    public String getHeroes_name() {
        return heroes_name;
    }

    public void setHeroes_name(String heroes_name) {
        this.heroes_name = heroes_name;
    }

    public int getHeroes_grade() {
        return heroes_grade;
    }

    public void setHeroes_grade(int heroes_grade) {
        this.heroes_grade = heroes_grade;
    }

    public String getHeroes_nickname() {
        return heroes_nickname;
    }

    public void setHeroes_nickname(String heroes_nickname) {
        this.heroes_nickname = heroes_nickname;
    }
}
